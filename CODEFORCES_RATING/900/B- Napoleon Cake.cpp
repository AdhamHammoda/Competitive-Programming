void test_case()
{
    ll n;
    cin>>n;
    ll a[n];
    for(int i=0;i<n;i++)cin>>a[i];
    ll ans[n];
    ll mn=1e18;
    for(int i=0;i<n;i++)ans[i]=0;
    for(int i=n-1;i>=0;i--)
    {
        mn=min(mn,i-a[i]);
        if(mn<i)ans[i]=1;
    }
    for(int i=0;i<n;i++)cout<<ans[i]<<" ";
    cout<<endl;
}
