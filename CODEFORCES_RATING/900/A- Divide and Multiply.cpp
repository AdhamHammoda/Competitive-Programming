#include<bits/stdc++.h>
#define FIO ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
using namespace std;
typedef long long ll;
typedef long double ld;
void test_case()
{
    ll n;
    cin>>n;
    ll a[n];
    ll c=0;
    for(int i=0;i<n;i++)
    {
        cin>>a[i];
        while(a[i]%2==0)a[i]/=2,c++;
    }
    sort(a,a+n);
    a[n-1]*=(1LL<<c);
    ll ans=0;
    for(int i=0;i<n;i++)ans+=a[i];
    cout<<ans<<endl;
}
