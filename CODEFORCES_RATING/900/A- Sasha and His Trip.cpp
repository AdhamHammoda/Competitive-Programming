#include<bits/stdc++.h>
#define FIO ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
using namespace std;
typedef long long ll;
typedef long double ld;
void test_case()
{
    ll n,v;
    cin>>n>>v;
    ll tank=0;
    ll ans=0;
    for(int i=1;i<=n;i++)
    {
        if(n-i==tank)break;
        if(n-i>tank)ans+=min(n-i,v-tank)*i,tank+=min(n-i,v-tank);
        tank--;
    }
    cout<<ans;
}
