#include<bits/stdc++.h>
#define FIO ios_base::sync_with_stdio(false);cin.tie(0);cout.tie(0);
using namespace std;
typedef long long ll;
typedef long double ld;
void test_case()
{
    ll n;
    cin>>n;
    ll arr[n];
    for(int i=0;i<n;i++)cin>>arr[i];
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<n;j++)
        {
            for(int k=0;k<n;k++)
            {
                if(arr[i]==arr[j] || arr[j]==arr[k] || arr[i]==arr[k])continue;
                ll mx=max({arr[i],arr[j],arr[k]});
                ll mn=min({arr[i],arr[j],arr[k]});
                if(mx-mn==2)cout<<"YES",exit(0);
            }
        }
    }
    cout<<"NO";
}
