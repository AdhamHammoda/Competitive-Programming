using namespace std;
typedef long long ll;
typedef long double ld;
void test_case()
{
    ll n,k;
    map<ll,ll>m;
    cin>>n>>k;
    ll mx=0;
    for(int i=1;i<=n;i++)
    {
        ll x;
        cin>>x;
        m[x]++;
        mx=max(mx,m[x]);
    }
    ll num=((mx/k)+(mx%k>0))*k;
    ll ans=0;
    for(auto x:m)ans+=(num-x.second);
    cout<<ans;
}
