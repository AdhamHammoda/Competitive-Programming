using namespace std;
typedef long long ll;
typedef long double ld;
void test_case()
{
    ll n,m;
    cin>>n>>m;
    char grid[n][m];
    for(int i=0;i<n;i++)
    {
        for(int j=0;j<m;j++)
        {
            cin>>grid[i][j];
        }
    }
    ll a[m];
    ll ans=0;
    for(int i=0;i<m;i++)cin>>a[i];
    for(int j=0;j<m;j++)
    {
        map<char,ll>mm;
        ll mx=0;
        for(int i=0;i<n;i++)
        {
            mm[grid[i][j]]++;
            mx=max(mx,mm[grid[i][j]]);
        }
        ans+=mx*a[j];
    }
    cout<<ans;
}
